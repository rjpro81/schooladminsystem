    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        //submits tasks to database
        int result = 0;
        try(Connection connection = DriverManager.getConnection(URL, username, password)){
            User user = new User();
            addTaskStatement = connection.prepareStatement(addTask);
            addTaskStatement.setString(1, taskTextField.getText());
            addTaskStatement.setString(2, dateTextField.getText());
            addTaskStatement.setString(3, timeTextField.getText());
            int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "Add Task", JOptionPane.YES_NO_OPTION);
            if (option == 0){
                result = addTaskStatement.executeUpdate();
                if (result > 0){
                    Date date = new Date();
                    Date taskDate = date.dateFromString(dateTextField.getText());
                    ToDo task = new ToDo(taskTextField.getText(), taskDate, timeTextField.getText());
                    JOptionPane.showMessageDialog(null, "Task added");
                    BusyBeeMainApplicationWindow.getToDoListTextArea().append(task.toString());
                    this.dispose();
                }
            }
        }
        catch(SQLException e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_submitButtonActionPerformed
